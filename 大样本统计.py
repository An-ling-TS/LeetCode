def sampleStats(count):
    """
    :type count: List[int]
    :rtype: List[float]
    """
    sum_=0.0
    max_=0
    mid=[0,0]
    mid_=-1
    min_=-1
    much=0
    n=0
    rs=[]
    for i in range(len(count)):
        if count[i]==0:
            continue
        sum_+=count[i]*i
        n+=count[i]
        if min_==-1:
            min_=i
        if count[i]>count[much]:
            much=i
        if i>max_:
            max_=i
    if n%2==0:
        mid=n/2,n/2+1
    else:
        mid=(n+1)/2,(n+1)/2
    temp=0
    for i in range(len(count)):
        temp+=count[i] 
        if temp>mid[1]:
            mid_=i
        if temp==mid[0]:
            mid_=i
            for j in range(i+1,len(count)):
                if count[j]>0:
                    mid_=float((mid_+j)/2.0)
                    break
        if mid_>-1:
            rs.append(float(min_))
            rs.append(float(max_))
            rs.append(float(sum_/n))
            rs.append(float(mid_))
            rs.append(float(much))
            return rs
        
print(sampleStats([0,1,3,4,0,0,0,0]))
